package qlttanhngu.dto;

/**
 *
 * @author EvilZ
 */
public class LoaiDeThiDTO {
    private String maLoaiDeThi;
    private String tenLoaiDeThi;
    private int thoiLuongthi;
    private String tenTrinhDo;

    /**
     * @return the maLoaiDeThi
     */
    public String getMaLoaiDeThi() {
        return maLoaiDeThi;
    }

    /**
     * @param maLoaiDeThi the maLoaiDeThi to set
     */
    public void setMaLoaiDeThi(String maLoaiDeThi) {
        this.maLoaiDeThi = maLoaiDeThi;
    }

    /**
     * @return the tenLoaiDeThi
     */
    public String getTenLoaiDeThi() {
        return tenLoaiDeThi;
    }

    /**
     * @param tenLoaiDeThi the tenLoaiDeThi to set
     */
    public void setTenLoaiDeThi(String tenLoaiDeThi) {
        this.tenLoaiDeThi = tenLoaiDeThi;
    }

    /**
     * @return the thoiLuongthi
     */
    public int getThoiLuongthi() {
        return thoiLuongthi;
    }

    /**
     * @param thoiLuongthi the thoiLuongthi to set
     */
    public void setThoiLuongthi(int thoiLuongthi) {
        this.thoiLuongthi = thoiLuongthi;
    }

    /**
     * @return the tenTrinhDo
     */
    public String getTenTrinhDo() {
        return tenTrinhDo;
    }

    /**
     * @param tenTrinhDo the tenTrinhDo to set
     */
    public void setTenTrinhDo(String tenTrinhDo) {
        this.tenTrinhDo = tenTrinhDo;
    }
    
}
