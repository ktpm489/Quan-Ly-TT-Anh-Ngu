package qlttanhngu.dto;

/**
 *
 * @author EvilZ
 */
public class TrinhDoDTO {
    private String maTrinhDo;
    private String tenTrinhDo;
    private String tenTrinhDoTienQuyet;

    /**
     * @return the maTrinhDo
     */
    public String getMaTrinhDo() {
        return maTrinhDo;
    }

    /**
     * @param maTrinhDo the maTrinhDo to set
     */
    public void setMaTrinhDo(String maTrinhDo) {
        this.maTrinhDo = maTrinhDo;
    }

    /**
     * @return the tenTrinhDo
     */
    public String getTenTrinhDo() {
        return tenTrinhDo;
    }

    /**
     * @param tenTrinhDo the tenTrinhDo to set
     */
    public void setTenTrinhDo(String tenTrinhDo) {
        this.tenTrinhDo = tenTrinhDo;
    }

    /**
     * @return the tenTrinhDoTienQuyet
     */
    public String getTenTrinhDoTienQuyet() {
        return tenTrinhDoTienQuyet;
    }

    /**
     * @param tenTrinhDoTienQuyet the tenTrinhDoTienQuyet to set
     */
    public void setTenTrinhDoTienQuyet(String tenTrinhDoTienQuyet) {
        this.tenTrinhDoTienQuyet = tenTrinhDoTienQuyet;
    }

    
    
}
