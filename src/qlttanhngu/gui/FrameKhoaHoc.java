    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            KhoaHocBO khoahocbo = new KhoaHocBO();
            KhoaHocDTO khoahocdto = new KhoaHocDTO();
            khoahocdto.setMaKhoaHoc(tblKhoaHoc.getValueAt(tblKhoaHoc.getSelectedRow(), 0).toString());
            khoahocbo.deleteKhoaHoc(khoahocdto);
            tblKhoaHoc.setModel((new KhoaHocController()).LoadListKhoaHoc());
        } catch (Exception ex) {
            Logger.getLogger(FrameKhoaHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

