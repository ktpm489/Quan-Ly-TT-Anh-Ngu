    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            // TODO add your handling code here:
            if(!"".equals(txtTenKhoaHoc.getText()) && dateKhaiGiang.getDate() != null){
                KhoaHocBO khoahocbo = new KhoaHocBO();
                KhoaHocDTO khoahocdto = new KhoaHocDTO();
                khoahocdto.setMaKhoaHoc("");
                khoahocdto.setTenKhoaHoc(txtTenKhoaHoc.getText());
                khoahocdto.setNgayKhaiGiang(dateKhaiGiang.getDate());
                
                khoahocbo.addKhoaHoc(khoahocdto);
                tblKhoaHoc.setModel((new KhoaHocController()).LoadListKhoaHoc());
            }
        } catch (Exception ex) {
            Logger.getLogger(FrameKhoaHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

