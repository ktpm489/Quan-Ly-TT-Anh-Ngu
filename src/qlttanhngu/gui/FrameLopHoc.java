    private String getKey(HashMap<String, String> hashMap ,String value) {
        try {
            for (Object s : hashMap.keySet()) {
                if (hashMap.get(s).equals(value)) {
                    return (String) s;
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(FrameLopHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    return null;
}
